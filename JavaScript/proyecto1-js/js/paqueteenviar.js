// Declarar dos variables, 'anchoPaquete' y 'altoPaquete', y asignar valores numéricos
let anchoPaquete = 10;  // en centímetros
let altoPaquete = 20;   // en centímetros 

// Definir una variable 'profundidadPaquete' y asignar un valor numérico constante
const profundidadPaquete = 15;  // en centímetros 

// Calcular el volumen del paquete
let volumenPaquete = anchoPaquete * altoPaquete * profundidadPaquete;

// Mostrar el resultado por consola
console.log(`El volumen del paquete es: ${volumenPaquete} cm³`);