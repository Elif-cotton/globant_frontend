// Math.abs(x): Devuelve el valor absoluto de x
let valorAbsoluto = Math.abs(-5);
console.log(`Valor absoluto de -5: ${valorAbsoluto}`);

// Math.ceil(x): Redondea hacia arriba al entero más cercano
let redondeoArriba = Math.ceil(5.3);
console.log(`Redondeo hacia arriba de 5.3: ${redondeoArriba}`);

// Math.floor(x): Redondea hacia abajo al entero más cercano
let redondeoAbajo = Math.floor(5.9);
console.log(`Redondeo hacia abajo de 5.9: ${redondeoAbajo}`);

// Math.round(x): Redondea al entero más cercano
let redondeoCercano = Math.round(5.6);
console.log(`Redondeo cercano de 5.6: ${redondeoCercano}`);

// Math.max(x1, x2, ..., xn): Devuelve el valor más grande entre los números proporcionados
let valorMaximo = Math.max(10, 20, 30, 5);
console.log(`Valor máximo: ${valorMaximo}`);

// Math.min(x1, x2, ..., xn): Devuelve el valor más pequeño entre los números proporcionados
let valorMinimo = Math.min(10, 20, 30, 5);
console.log(`Valor mínimo: ${valorMinimo}`);

// Math.random(): Devuelve un número decimal aleatorio entre 0 (incluido) y 1 (excluido)
let numeroAleatorio = Math.random();
console.log(`Número aleatorio entre 0 y 1: ${numeroAleatorio}`);

let numeroAleatorio1 = Math.floor(Math.random()*10)+1;
console.log(`Número aleatorio entre 1 y 10: ${numeroAleatorio1}`);